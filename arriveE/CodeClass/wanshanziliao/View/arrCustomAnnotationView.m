#import "arrCustomAnnotationView.h"
#define kCalloutWidth       200.0
#define kCalloutHeight      70.0
@interface arrCustomAnnotationView ()

@property (nonatomic, strong, readwrite) arrCustomCalloutView *calloutView;

@end
@implementation arrCustomAnnotationView



/*
